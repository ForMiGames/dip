package com.example.telecomsim.model.channel;

import lombok.Builder;
import lombok.Getter;
/**
 * Параметры симулируемого канала связи.
 *
 * Все параметры описывают физические и протокольные характеристики канала,
 * которые влияют на результат симуляции передачи данных.
 */
@Getter
@Builder
public class ChannelParameters {
// ── Идентификация ─────────────────────────────────────────────────────

    /** Тип среды передачи данных. */
    private final ChannelType channelType;

    // ── Производительность ────────────────────────────────────────────────

    /**
     * Пропускная способность канала (бит/с).
     * Определяет максимальную скорость передачи данных.
     * Примеры: 1_000_000 (1 Мбит/с), 1_000_000_000 (1 Гбит/с).
     */
    @Builder.Default
    private final long bandwidthBps = 100_000_000L; // 100 Мбит/с по умолчанию

    // ── Задержки ──────────────────────────────────────────────────────────

    /**
     * Базовая задержка обработки канала (мс).
     * Не включает задержку распространения (она считается из distanceKm).
     */
    @Builder.Default
    private final long latencyMs = 20L;

    /**
     * Максимальный джиттер — разброс задержки (мс).
     * Фактический джиттер для каждого пакета генерируется случайно
     * в диапазоне [-jitterMs; +jitterMs] по гауссову распределению.
     */
    @Builder.Default
    private final long jitterMs = 5L;

    // ── Ошибки ────────────────────────────────────────────────────────────

    /**
     * Коэффициент битовых ошибок (Bit Error Rate, BER).
     * Вероятность того, что каждый отдельный бит будет искажён при передаче.
     *
     * Типичные значения:
     *  Оптоволокно:  1e-12 (практически без ошибок)
     *  Ethernet:     1e-8
     *  Wi-Fi:        1e-5
     *  4G/LTE:       1e-6
     *  Спутник:      1e-7
     */
    @Builder.Default
    private final double bitErrorRate = 1e-6;

    /**
     * Вероятность потери пакета (Packet Loss Rate).
     * Значение от 0.0 (нет потерь) до 1.0 (все пакеты теряются).
     * Примеры: 0.001 (0.1%), 0.05 (5%).
     */
    @Builder.Default
    private final double packetLossRate = 0.001;

    // ── Пакетизация ───────────────────────────────────────────────────────

    /**
     * Maximum Transmission Unit — максимальный размер пакета (байт).
     * Включает заголовки протоколов (IP + TCP/UDP ≈ 40 байт).
     *
     * Стандартные значения:
     *  Ethernet:   1500 байт
     *  Wi-Fi:      2304 байт (но обычно 1500 для совместимости)
     *  DSL:        1492 байт
     *  Jumbo frame:9000 байт
     */
    @Builder.Default
    private final int mtuBytes = 1500;

    // ── Физические параметры среды ────────────────────────────────────────

    /**
     * Расстояние между отправителем и получателем (км).
     * Используется для расчёта задержки распространения сигнала.
     * 0 — расстояние не учитывается (локальная сеть).
     */
    @Builder.Default
    private final double distanceKm = 0.0;

    /**
     * Отношение сигнал/шум (Signal-to-Noise Ratio, дБ).
     * Информационный параметр — отображается в UI.
     * Косвенно коррелирует с BER, но в симуляции BER задаётся напрямую.
     *
     * Типичные значения:
     *  Отличный сигнал: > 25 дБ
     *  Хороший:         15–25 дБ
     *  Удовлетворительный: 10–15 дБ
     *  Плохой:          < 10 дБ
     */
    @Builder.Default
    private final double signalToNoiseRatioDb = 25.0;

    // ── Вычисляемые свойства ──────────────────────────────────────────────

    /** Пропускная способность в Мбит/с (для отображения в UI). */
    public double getBandwidthMbps() {
        return bandwidthBps / 1_000_000.0;
    }

    /** Пропускная способность в Гбит/с (для отображения в UI). */
    public double getBandwidthGbps() {
        return bandwidthBps / 1_000_000_000.0;
    }

    /** Потеря пакетов в процентах (для отображения в UI). */
    public double getPacketLossPercent() {
        return packetLossRate * 100.0;
    }

    /**
     * Теоретическая задержка распространения (мс).
     * Скорость света в оптоволокне ≈ 200 000 км/с = 200 км/мс.
     */
    public long getPropagationDelayMs() {
        if (distanceKm <= 0) return 0;
        return Math.round(distanceKm / 200.0);
    }

    /** Суммарная минимальная задержка (обработка + распространение), мс. */
    public long getTotalMinLatencyMs() {
        return latencyMs + getPropagationDelayMs();
    }
}
